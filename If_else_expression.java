package How_To_Run_JAVA;

public class If_else_expression {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;
        int max = 0;

        if (a > b) {
            max = a;
        } else {
            max = b;
        }

        System.out.println("The maximum value is = " + max);
    }
}
